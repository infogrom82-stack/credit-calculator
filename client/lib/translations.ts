export function getTranslation(language: Language): TranslationKeys {
  return translations[language] || translations.ru;
}

export function formatTermLabel(term: number, termType: "months" | "years", language: Language): string {
  const t = getTranslation(language);
  
  if (termType === "months") {
    return `${term} ${t.months.toLowerCase()}`;
  }
  
  if (language === "ru") {
    if (term === 1) return `${term} ${t.year}`;
    if (term >= 2 && term <= 4) return `${term} ${t.yearPlural}`;
    return `${term} ${t.yearPlural2}`;
  } else if (language === "pl") {
    if (term === 1) return `${term} ${t.year}`;
    if (term >= 2 && term <= 4) return `${term} ${t.yearPlural}`;
    return `${term} ${t.yearPlural2}`;
  }
  
  if (term === 1) return `${term} ${t.year}`;
  return `${term} ${t.yearPlural}`;
}
